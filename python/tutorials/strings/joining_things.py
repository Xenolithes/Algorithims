flowers = [
    "Daffodil",
    "Eveining Primrose",
    "Hydrangea",
    "Iris",
    "Lavander",
    "Sunflower",
    "Tiger Lily"
]

seperator = " | "
output = seperator.join(flowers)
print(output)

